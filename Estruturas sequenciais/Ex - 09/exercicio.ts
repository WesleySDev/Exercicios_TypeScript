/* 
Faça um programa que parte a temperatura em graus Farenheit, transforma e mostra a temperatura em graus Celsius. C=(5*(F-32)/9).
*/

let teemperaturaFahrenheit: number = 100;
let teemperaturaCelsius: number = (5 * (temperaturaFahrenheit - 32)) / 9;
console.log(
  `A temperatura de ${temperaturaFahrenheit}°F é equivalente a ${temperaturaCelsius.toFixed(
    2
  )}°C.`
);
